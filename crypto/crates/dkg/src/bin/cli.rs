#[derive(Parser, Debug)]
#[command(name = "dkg", version, about, long_about = None)]
struct Cli {
    #[command(subcommand)]
    command: Commands,

    #[arg(long, global = true)]
    verbose: bool,
}

#[derive(Subcommand, Debug)]
enum Commands {
    /// Generate commitments and shares for DKG
    GenerateShares(GenerateShares),
    /// Read participant received file and verify the shares and commitments
    /// received from each of other participants
    VerifyPedersen(VerifyPedersen),
    VerifyFeldman(VerifyFeldman),
    /// Derive share key and public key
    DeriveKeys(DeriveKeys),
}

#[derive(Args, Debug)]
struct GenerateShares {
    /// Participant ID
    #[arg(long)]
    participant_id: usize,

    /// How many participants
    #[arg(long)]
    participants: usize,

    /// Minimal number
    #[arg(long)]
    threshold: usize,
}

impl GenerateShares {
    fn execute(self) -> Result<(), String> {
        if self.participant_id < 1 || self.participant_id > self.participants {
            return Err(format!("participant_id is not valid"));
        }

        let random_secret = gen_rand_biguint();

        let pedersen_deal_result = vss::deal(vss::DealParams {
            secret: random_secret,
            players: self.participants,
            threshold: self.threshold,
        })?;

        // Feldman commitments
        let feldman_commitments = feldman_commitments(pedersen_deal_result.coeffs_a);

        let output = Output::new(self.participant_id);

        let mut shares_hashmap: HashMap<String, ParticipantShare> = HashMap::new();
        for share in pedersen_deal_result.shares {
            shares_hashmap.insert(
                share.0.to_string(),
                ParticipantShare {
                    s: biguint_to_hex(&share.1),
                    u: biguint_to_hex(&share.2),
                },
            );
        }

        for i in 1..=self.participants {
            let output = Output::new(i);
            let received_share = shares_hashmap.get(&i.to_string()).cloned().unwrap();
            output
                .append_received(
                    self.participant_id.to_string(),
                    pedersen_deal_result
                        .commitments
                        .iter()
                        .map(|v| biguint_to_hex(&v))
                        .collect(),
                    feldman_commitments
                        .iter()
                        .map(|v| biguint_to_hex(&v))
                        .collect(),
                    received_share,
                )
                .unwrap();
        }

        output
            .write_generated(
                self.participant_id,
                pedersen_deal_result
                    .commitments
                    .iter()
                    .map(|v| biguint_to_hex(&v))
                    .collect(),
                feldman_commitments
                    .iter()
                    .map(|v| biguint_to_hex(&v))
                    .collect(),
                shares_hashmap,
            )
            .expect("write generated result fail");

        println!(
            "Shares are generated by participant {}",
            self.participant_id
        );

        Ok(())
    }
}

#[derive(Args, Debug)]
struct VerifyPedersen {
    /// Participant ID that will be read data from
    #[arg(long, short)]
    participant_id: usize,

    /// How many participants
    #[arg(long)]
    participants: usize,
}

impl VerifyPedersen {
    fn execute(self) -> Result<(), String> {
        let participant_id = self.participant_id;
        let participants = self.participants;
        let output = Output::new(participant_id);
        let received_info = output
            .read_received()
            .or_else(|err| Err(format!("cannot read participant file, error: {}", err)))?;

        if received_info.pedersen_commitments.len() != participants
            || received_info.shares_from.len() != participants
        {
            return Err("have not received data of all participants".into());
        }

        // verify each received share with corresponding pedersen commitments
        for (_, (p_id, ParticipantShare { s, u })) in received_info.shares_from.iter().enumerate() {
            // get share received from pariticipant p_id
            let share: (usize, BigUint, BigUint) =
                (participant_id, hex_to_biguint(&s)?, hex_to_biguint(&u)?);

            // get corresponding received from pariticipant p_id
            let mut pedersen_commitments: Vec<BigUint> = vec![];
            if let Some(s) = received_info
                .pedersen_commitments
                .get(&p_id.to_string())
                .cloned()
            {
                for c in s {
                    pedersen_commitments.push(hex_to_biguint(&c.as_str())?);
                }
            } else {
                return Err(format!(
                    "cannot load Pedersen commitments of pariticipant {} from received data of participant {}",
                    p_id, participant_id
                ));
            }

            let verify_result = vss::verify(VerifyParams {
                commitments: pedersen_commitments,
                share: share,
            })?;

            println!(
                "Participant: {} , Result {}, Commit Value: {}, Share Value: {}",
                p_id,
                verify_result.result,
                verify_result.verify_commitment_value,
                verify_result.verify_share_value
            );

            if !verify_result.result {
                output.save_complaint_pedersen(p_id.clone())?;
                // broadcast complaint
                for id in 1..=self.participants {
                    output.send_complaint_pedersen(
                        id,           // to
                        p_id.clone(), // against
                    );
                }
            }
        }

        Ok(())
    }
}

#[derive(Args, Debug)]
struct VerifyFeldman {
    #[arg(long)]
    participant_id: usize,
}

impl VerifyFeldman {
    fn execute(self) -> Result<(), String> {
        // load received
        let output = Output::new(self.participant_id);
        let received = output.read_received()?;

        for (_, (sender_id, commitments_str)) in received.feldman_commitments.iter().enumerate() {
            let mut commitments: Vec<BigUint> = vec![];
            for v in commitments_str {
                commitments.push(hex_to_biguint(&v)?);
            }

            // calculate share value
            let share = received
                .shares_from
                .get(sender_id)
                .cloned()
                .ok_or(format!("cannot read share from {}", sender_id))?;

            let result =
                feldman_verify(self.participant_id, commitments, hex_to_biguint(&share.s)?)?;

            // Compare
            println!(
                "Sender: {}, Result: {}, Commitment Value: {}, Share Value: {}",
                sender_id, result.0, result.1, result.2
            );
        }

        Ok(())
    }
}

#[derive(Debug, Args)]
struct DeriveKeys {
    #[arg(long)]
    participant_id: usize,
}

impl DeriveKeys {
    fn execute(&self) -> Result<(), String> {
        let output = Output::new(self.participant_id);
        let received = output.read_received()?;

        // derive share key
        let mut shares: Vec<BigUint> = vec![];
        for (_, (_, ParticipantShare { s, u: _ })) in received.shares_from.iter().enumerate() {
            shares.push(hex_to_biguint(&s)?);
        }
        output.write_share_key(&gennaro_derive_key_share(shares)?)?;

        // derive public key
        let mut commitments: HashMap<String, Vec<BigUint>> = HashMap::new();
        for (p_id, c_hex_vec) in received.feldman_commitments {
            commitments.insert(
                p_id,
                <HexVec as TryInto<BigUintVec>>::try_into(HexVec(c_hex_vec))?.0,
            );
        }
        output.write_public_key(&feldman_derived_public_key(commitments)?)?;
        Ok(())
    }
}

fn main() -> Result<(), String> {
    let args = Cli::parse();
    // let fmt = &args.number_format;
    // let _verbose = args.verbose;

    match args.command {
        Commands::GenerateShares(args) => args.execute(),
        Commands::VerifyPedersen(args) => args.execute(),
        Commands::VerifyFeldman(args) => args.execute(),
        Commands::DeriveKeys(args) => args.execute(),
    }
}
